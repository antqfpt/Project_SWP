    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        response.setContentType("application/json");
        response.setCharacterEncoding("UTF-8");

        HttpSession session = request.getSession();
        Accounts account = (Accounts) session.getAttribute("account");

        if (account != null) {
            EmployeeDAO employeeDAO = new EmployeeDAO();
            Employees employee = employeeDAO.getEmployeeById(employeeDAO.getEmployeeIdByAccountId(account.getAccount_id()));

            Map<String, Object> userInfo = new HashMap<>();
            userInfo.put("name", employee.getEmployee_name());
            userInfo.put("email", account.getEmail());
            userInfo.put("phone", employee.getEmployee_phone());
            userInfo.put("address", employee.getEmployee_address());

            String json = new Gson().toJson(userInfo);
            PrintWriter out = response.getWriter();
            out.print(json);
            out.flush();
        } else {
            response.setStatus(HttpServletResponse.SC_UNAUTHORIZED);
            response.getWriter().write("User not logged in");
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

