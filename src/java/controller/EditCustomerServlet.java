    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("application/json");
        response.setCharacterEncoding("UTF-8");

        try {
            int customerId = Integer.parseInt(request.getParameter("id"));
            CustomerDAO customerDAO = new CustomerDAO();
            Customers customer = customerDAO.getCustomerById(customerId);

            if (customer != null) {
                String jsonCustomer = new Gson().toJson(customer);
                response.getWriter().write(jsonCustomer);
            } else {
                response.setStatus(HttpServletResponse.SC_NOT_FOUND);
                response.getWriter().write("{\"error\": \"Customer not found\"}");
            }
        } catch (NumberFormatException e) {
            response.setStatus(HttpServletResponse.SC_BAD_REQUEST);
            response.getWriter().write("{\"error\": \"Invalid customer ID\"}");
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("application/json");
        response.setCharacterEncoding("UTF-8");

        try {
            int customerId = Integer.parseInt(request.getParameter("customerId"));
            String customerName = request.getParameter("customerName");
            String customerPhone = request.getParameter("customerPhone");
            int customerTypeId = Integer.parseInt(request.getParameter("customerType"));

            Customers customer = new Customers();
            customer.setCustomerId(customerId);
            customer.setCustomerName(customerName);
            customer.setCustomerPhone(customerPhone);

            CustomerType type = new CustomerType();
            type.setCustomerTypeId(customerTypeId);
            customer.setCustomerType(type);

            CustomerDAO customerDAO = new CustomerDAO();
            boolean success = customerDAO.updateCustomer(customer);

            String jsonResponse = "{\"success\": " + success + "}";
            response.getWriter().write(jsonResponse);
        } catch (Exception e) {
            response.setStatus(HttpServletResponse.SC_BAD_REQUEST);
            response.getWriter().write("{\"error\": \"" + e.getMessage() + "\"}");
        }
    }

